export const convertStrTime2NumTime = strTime => {
  let year = parseInt(strTime.substring(2));
  if (strTime.substring(0, 2) === "BC") year *= -1;
  return year;
};

export const convertNumTime2StrTime = numTime => {
  return numTime < 0 ? "BC" + -1 * numTime : "AD" + numTime;
};

